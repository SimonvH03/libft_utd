#include "libft.h"

/**
 * @brief Adds a new element to the front of a linked list.
 *
 * This function adds the element @p new to the front of the linked list @p lst.
 *
 * @param lst The address of a pointer to the first element of the linked list.
 * @param new The element to add to the linked list.
 */
void	ft_lstadd_front(t_list **lst, t_list *new)
{
	if (new == NULL)
		return ;
	new->next = *lst;
	*lst = new;
}
